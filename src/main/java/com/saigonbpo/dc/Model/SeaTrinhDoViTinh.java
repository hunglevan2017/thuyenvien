package com.saigonbpo.dc.Model;

public class SeaTrinhDoViTinh {
    private Integer id;

    private String tenphanmem;

    private String xeploai;

    private Integer thuyenvienid;

    private String ghichu;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTenphanmem() {
        return tenphanmem;
    }

    public void setTenphanmem(String tenphanmem) {
        this.tenphanmem = tenphanmem;
    }

    public String getXeploai() {
        return xeploai;
    }

    public void setXeploai(String xeploai) {
        this.xeploai = xeploai;
    }

    public Integer getThuyenvienid() {
        return thuyenvienid;
    }

    public void setThuyenvienid(Integer thuyenvienid) {
        this.thuyenvienid = thuyenvienid;
    }

    public String getGhichu() {
        return ghichu;
    }

    public void setGhichu(String ghichu) {
        this.ghichu = ghichu;
    }
}